function exemploFor () {

for (let contador = 1; contador <= 5; contador++) {
    
    alert(contador);

    } 
}

function exemploDoWhile () {

    let contador = 1;

    while (contador <= 5) {

        console.log(contador);
        contador ++;

    } while (contador <= 5);

}

